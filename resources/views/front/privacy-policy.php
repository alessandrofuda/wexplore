<p><strong>Dove conserviamo i dati personali?</strong></p>
<p>Il Titolare conserva i dati personali in archivi cartacei all’interno della sede del Titolare, oltre ad archivi informatici situati sia all’interno dell’Unione Europea, sia al di fuori qualora ciò sia strumentale al perseguimento delle finalità indicate nella lettera a). In tale ultimo caso il Titolare si assicura che società non aventi sedi all’interno dell’Unione Europea stiano trattando con la massima riservatezza i dati personali nel rispetto delle decisioni di adeguatezza della Commissione Europea, di eventuali Privacy Shield o, qualora necessario, stipulando accordi che garantiscano un adeguato livello di protezione.</p>
<p>&nbsp;</p>
<p><strong>Come vengono trattati i dati personali?</strong></p>
<p>Il Titolare tratta i dati personali degli Interessati adottando le idonee misure di sicurezza volte ad impedire l’accesso, la divulgazione, la modifica e la distruzione non autorizzate.</p>
<p>Il trattamento dei dati è eseguito attraverso procedure informatiche, mezzi telematici e, in via residuale, su supporti cartacei da parte di soggetti interni appositamente autorizzati nonché dai responsabili esterni in caso nominati, e ciò anche in base agli accordi contrattuali in essere.</p>
<p>&nbsp;</p>
<p><strong>Qual è la politica sul trattamento dati dei minori?</strong></p>
<p>Il Titolare è a conoscenza della delicatezza del trattamento dati dei minori. In particolare i Servizi non vogliono essere erogati a minori di anni 14 e il Titolare non tratta volontariamente dati di minori di 14 anni: in tal senso, si richiede agli Interessati di non richiedere l’erogazione dei Servizi nel caso l’età sia minore di 14 anni.</p>
<p>Il Titolare incoraggia chi esercita la responsabilità genitoriale sui minori di 14 anni a controllare che questi non richiedano l’erogazione dei Servizi e, in ogni caso, ad educare i minori di 14 anni a non rilasciare i loro dati personali tramite il Sito.</p>
<p>Nel caso il Titolare venga a conoscenza che alcuni dati personali si riferiscono a minori di 14 anni, il Titolare prenderà provvedimenti per cancellare il dato personale.</p>
<p>&nbsp;</p>
<p><strong>Cosa succede se ci sono link ad altri siti web?</strong></p>
<p>Il Titolare informa gli Interessati che la presente Informativa si applica solo al Sito e, nel caso vi siano link ad altri siti web, l’Interessato dovrà verificare le informative di detti siti prima di rilasciare i propri dati personali.</p>
<p>Il Titolare non si prende alcuna responsabilità per i dati personali forniti dagli Interessati in altri siti web.</p>
<p><strong>Cambiamenti all’Informativa</strong></p>
<p>Il Titolare si riserva il diritto di modificare la presente Informativa in qualsiasi momento. In caso di modifiche il Titolare caricherà su questa pagina la nuova informativa e, in tal senso, si esorta l’Interessato a controllare le modifiche dell’Informativa: l’Interessato potrà vedere lo storico delle informative controllando la data apposta.</p>
<p>Continuando ad utilizzare il Sito successivamente alle modifiche, l’Interessato accetta tali modifiche e acconsente al trattamento dati così come modificato.</p>
