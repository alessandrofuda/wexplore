<p><strong>Cosa accade se l’Interessato non fornisce i necessari dati personali?</strong></p>
<p>Qualora i dati risultino necessari per erogare i Servizi e per Fornire supporto agli Interessati, il Titolare non sarà in grado di erogarli e supportare l’Interessato nelle proprie richieste. In tal caso il Titolare potrà, alternativamente, richiedere l’integrazione dei dati personali o cancellare i dati personali dell’Interessato impedendo l’erogazione dei Servizi.</p>
<p>Per finalità diverse dall’erogazione dei Servizi e dal Fornire supporto agli Interessati, il conferimento dei dati è facoltativo e il mancato conferimento dei dati personali non influirà sulle menzionate finalità di Trattamento.</p>
