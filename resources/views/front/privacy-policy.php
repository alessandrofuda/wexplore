<p><strong>Dati aggregati</strong></p>
<p>Il Titolare può raccogliere, utilizzare e condividere dati aggregati, come dati statistici o demografici, per qualsiasi finalità.</p>
<p>I dati aggregati possono derivare dai dati personali dell’Interessato, ma una volta aggregati non costituiscono un dato personale ai sensi del GDPR in quanto non sono in grado di identificare direttamente o indirettamente l’Interessato. Tuttavia, se il Titolare combina o connette i dati aggregati con i dati personali dell’Interessato di modo da permettere l’identificazione dell’Interessato, direttamente o indirettamente, il Titolare tratterà il dato così risultante secondo quanto disposto dall’Informativa.</p>
<p>&nbsp;</p>
<p><strong>Dati Particolari</strong></p>
<p>Il Titolare non tratta nessuna categoria di dati particolari dell’Interessato (per dato particolare si intende un dato relativo a origine etnica o raziale, convinzioni religiose o filosofiche, orientamento sessuale, opinioni politiche, appartenenza sindacale, dati genetici, biometrici e di salute), come non tratta nessun dato relativo a condanne penali e reati relativi all’Interessato.</p>
