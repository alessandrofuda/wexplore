<p><strong>INFORMATIVA PER IL TRATTAMENTO DEI DATI PERSONALI&nbsp;&nbsp;&nbsp; Maggio 2019</strong></p>
<p>La società Wexplore S.r.l. (P. IVA 03560330130), con sede legale alla Via Sangallo 33 in in Milano, in qualità di titolare del trattamento dati personali (di seguito il “<strong>Titolare</strong>”) del sito web <a href="http://www.wexplore.eu">www.wexplore.eu</a> (di seguito il “<strong>Sito</strong>”), informa i visitatori del Sito (di seguito gli “<strong>Interessati</strong>”) ai sensi dell’art. 13 del regolamento europeo n. 2016/679, il General Data Protection Regulation (GDPR).</p>
<p>&nbsp;</p>
<p>Il Titolare è a conoscenza dell’importanza del trattamento dei dati personali degli Interessati e, per questo motivo, si prende cura di indicare quali dati vengono trattati e come questi vengono trattati. Procedendo nella navigazione del Sito o indicando la volontà di usufruire dei servizi forniti dallo stesso, l’Interessato dichiara di aver letto e accettato la presente informativa (di seguito “<strong>Informativa</strong>”), rilasciando così il consenso per il trattamento dei dati personali da parte del Titolare.</p>
<p>&nbsp;</p>
<p>Per qualsiasi informazione, dubbio o richiesta relativi alla presente Informativa, il Titolare mette a disposizione degli Interessati il seguente indirizzo email:</p>
<p><a href="mailto:info@wexplore.eu">info@wexplore.eu</a></p>
<p><strong>Quali sono i diritti dell’Interessato in relazione al trattamento dei dati personali?</strong></p>
<p>L’Interessato ha i seguenti diritti:</p>
<ul>
<li>diritto di essere informato che vi sia un trattamento dati in essere che lo riguarda e, in caso positivo, accedere ai dati personali trattati;</li>
<li>diritto di rettifica dei dati personali;</li>
<li>diritto alla cancellazione (diritto all’oblio) dei dati personali che lo riguardano;</li>
<li>diritto alla limitazione del trattamento dei dati personali che lo riguardano;</li>
<li>diritto alla portabilità dei dati per ricevere, o far trasmettere ad altro Titolare, i dati personali che lo riguardano in formato strutturato, di uso comune e leggibile da dispositivo automatico;</li>
<li>diritto di opporsi al trattamento dei dati personali;</li>
<li>diritto di ritirare il consenso precedentemente rilasciato;</li>
<li>diritto di effettuare reclamo alle autorità compenti per violazione del trattamento dati personali.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Come esercitare i diritti?</strong></p>
<p>L’Interessato potrà esercitare i propri diritti scrivendo all’indirizzo email sopra indicato.</p>
<p>Il Titolare non intende far sostenere nessun costo agli Interessati per esercitare uno dei loro diritti, ma per far ciò il Titolare potrebbe richiedere specifiche informazioni per dar seguito alle comunicazioni dell’Interessato in relazione ai diritti.</p>
<p>Le menzionate comunicazioni vengono solitamente riscontrate entro 30 giorni dalla ricezione della comunicazione stessa, ma in caso questo termine non potrà essere rispettato (ad es. per eccessivo carico di richieste o complessità della risposta) sarà cura del Titolare comunicarlo all’Interessato e mantenerlo aggiornato sugli sviluppi della comunicazione inviata.</p>
<p>&nbsp;</p>
<p><strong>Quali dati personali vengono trattati?</strong></p>
<p>Il Titolare tratta i dati personali che gli vengono forniti sia dall’Interessato che da terze parti al fine di poter da seguito alle richieste di contatto dell’Interessato pervenute tramite il Sito (di seguito i “<strong>Servizi</strong>”).</p>
